def func():
    m = float(input("Введіть норму пробігу що спортсмен пробігає за 1-й день [M]: "))
    k = float(input("Введіть процент збільшення норми пробігу [К%]: "))
    distance = 50

    days = 1
    while m < distance:
        m += m * ( k/100)
        days += 1

    print(f"Норма пробігу стане більше 50 км через {days} днів.")